package com.example.assignmentcalculator

// all math logic will go here
class Math {
    fun addNumbers(a: Float, b: Float): Float{
        return a + b
    }
    //subtract numbers
    fun subtractNumber(a: Float, b: Float): Float{
        return a - b
    }
    //multiplying
    fun multiplyNumbers(a: Float, b: Float): Float{
        return a * b
    }
    //dividing
    fun divideNumbers(a: Float, b: Float): Float{
        return a / b
    }
}